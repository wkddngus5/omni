package com.pgt.bike.lock.lib.entity;

public class BikeConfigCommandVo extends BaseCommandVo{
	 
	private int lightMode;
	private int speedMode;
	private int acceleratorMode;
	private int amode;
	 
	
	
	
	public BikeConfigCommandVo() {
		super();
	}




	public int getLightMode() {
		return lightMode;
	}




	public void setLightMode(int lightMode) {
		this.lightMode = lightMode;
	}




	public int getSpeedMode() {
		return speedMode;
	}




	public void setSpeedMode(int speedMode) {
		this.speedMode = speedMode;
	}




	public int getAcceleratorMode() {
		return acceleratorMode;
	}




	public void setAcceleratorMode(int acceleratorMode) {
		this.acceleratorMode = acceleratorMode;
	}




	public int getAmode() {
		return amode;
	}




	public void setAmode(int amode) {
		this.amode = amode;
	}
	
	 
	
	
	
	
	

}
